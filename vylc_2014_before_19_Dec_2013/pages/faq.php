  <div id="faqs-page" class="page">
    <div class="seq"><span>General</span>   </div>

    <p><b>When is VYC’14?</b><br/>
        
      The Convention is from 10th Jan 2014 to 13th Jan 2014. College Competitions are on 12th Jan 2013 and School Competitions are on 11th Jan 2013.
    </p>

    <div class="seq"><span>Events</span>   </div>
    <p><b>  Who can participate in VYC’14? </b><br/>
      The talks and the workshop are open to all.
      For other events, all colleges and schools in and around Kanpur and Lucknow are invited to participate in the Convention. Teachers cannot participate in the competitions.<br/>
    </p>
    <br/><p><b>  How can I participate in VYC’14?</b><br/>
      For Talks, there is no registration.
      For School events, registration can be done by the school authorities only. They can select students for every competition download the file from Registration tab fill it and send to vsamiti@iitk.ac.in with subject VYC School Name. An example is given in the file. For other events, College students can register for the events in which they want to participate from this website. Login from Registration tab and register into various events from Events tab.
    </p>
    <br/>
    <br/><p><b> Will logistics be arranged by the institute?</b><br/>
        No travel arrangements will be made by the institute to reach IITK from various schools and colleges. Also no accommodation will be provided to students of local colleges. Limited accommodation to outstation participants are available, in which case, participants need to send us (vsamiti@iitk.ac.in) their travel and stay plan latest by Jan.5, 2013.<br/>
    </p>

    <br/> <p> <b>As an individual, can I participate in the school events?</b><br/>
      No, only students whose entries are provided by the schools are allowed to participate in the school events. You can request your school authorities to recommend your name.<br/>
    </p>

    <br/> <p> <b>Can one school student participate in more than one competition?</b><br/>
        No, one student can participate in only one of the competitions.<br/>
    </p>
    <br/> <p> <b>How many names can a school give for "Career Counselling"?</b><br/>
        Each school can give names of maximum 15 students for Career Counselling.<br/>
    </p>
    <br/> <p> <b>Do we have to register names of all the participants of one team in "Stage Play"?</b><br/>
        No, only the leader of the team is required to register for the stage play.<br/>
    </p>
    <br/> <p> <b>Do we have to register for the "Workshop on Leadership and Soft skills"?</b><br/>
        Yes, you have to register for the workshop. No participation certificates will be provided for the workshop. In case of large number of registrations preference will be given to people who registered first.<br/>
    </p>

    <br/>  <b>Will prizes and certificates be given for each competition?</b><br/>
        Prizes and certificates will be given to top 3 participants in each competition and participation certificates will be given to all the participants.
    <br/><div class="seq"><span>Talks<br/></span> </div>
    <b> Who can attend the "Talks"?</b><br/>
        The talks are open to all, both inside and outside campus community.<br/><br/>

    <b> Do we have to register to attend the "Talks"?</b><br/>
        No, registration is not required for talks.<br/>

