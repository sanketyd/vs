<div id="enlightened_citizenship" style="border: 2px solid black">  	
 	<span style='font-size:25px; margin-left:31px; margin-top:0px;border-bottom: 2px solid black'>Workshop on Enlightened Citizenship - Citizen Cafe (Youth Cafe)</span>
 		<p style="font-size: 11.5pt; margin-left:31px; color:black;">Sunday, 12th Jan., 2014<br/>
		Batch 1: 10 am -1 pm<br/>
		Batch 2: 2 pm - 5 pm<br/>
		Venue: L-15, Lecture Hall Complex, IIT Kanpur<br/>
		<b>Topic</b>: <br/>
		This workshop is launched by the non-government organisation ‘We, the people’. It is an effort to spread awareness on the preamble to the Constitution and its underlying responsibilities among citizen. Vivekananda Youth Leadership Convention 2014 at IIT Kanpur is one of the 20 destinations of this workshop, spread among seven cities in India. The Cafe uses the Preamble of the Constitution of India as the main anchor of the discussions and helps citizens derive the meaning and action from this visionary document. It explores what has worked and what we can do to ‘live’ our citizenship in our personal, social lives and in our interactions with the State. It intends to make citizens think, put them in touch with citizens who want change and move them to action
		</p>
		<p style="font-size: 11.5pt; margin-left:31px; color:black;">
		While working on social development, they found that most people are ignorant about India as a democracy and constantly criticize the country. This cafe is conducted at colleges and companies, and is part of a pan-India initiative called Constitution Connect. The first two seasons of this cafe were held in Mumbai during Nov 26th and 29th, 2013.The entire approach is very informal, in a coffee shop or cafe kind of atmosphere, to help people participate and be active in a democracy without being conscious
		</p>
		<p style="font-size: 11.5pt; margin-left:31px; color:black;">
		This workshop carries main its motive to educate people towards developing tools for struggling with any kind of government issue. Like if some group of people want to admit underprivileged children in private school using RTE act, or, people in a mohalla want to fight against problem in their lacality using RTI act, and so on. Businessmen, teachers, students, social activists and ex-army men come together for this cafe to reflect over the importance and relevance of the Indian Constitution.
		</p>
		<p style="font-size: 11.5pt; margin-left:31px; color:black;">
		Those who have attended the sessions earlier, feel that they see a lot of other opinions and differing positions. And all of them are right in their own way.They feel more encouraged to do their own part. They feel that Citizen Cafes are useful for one reason – spreading awareness. There is far too much of a feeling of helplessness. They all know something needs to be done but don’t know what to do. What stands out is the fact that people are really interested in getting involved. 
		</p>
		<p style="font-size: 11.5pt; margin-left:31px; color:black;">
		The workshop will be facilitating the participants in a group of few, divided in two slots in a coffee shop chat style.
		</p>
