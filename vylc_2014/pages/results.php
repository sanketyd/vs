		<p>
			The second round evaluates the social importance of your idea. For participating in the second round, you are required to make a presentation on your idea and present it among the jury. You will be given 15 minutes for presentation and 5 minutes for question - answer deliberations. The presentation shall be made on the following outlines:

			<ul>
				<li>Description of the problem which you are going to solve, or description of the new need that you have discovered</li>
				<li>How does this issue affect people in mass</li>
				<li>What is the work done by you till now</li>
				<li>What is the work done by other people on the same</li>
				<li>What is the work you have planned to do</li>
				<li>What large changes your idea will bring into</li>
				<li>Location where you will be executing your plan</li>
			</ul>
		</p>

		<p>
			The third round evaluates the business feasibility. For participating in the third round, you are required to make a presentation on your idea and present it among the jury. You will be given 15 minutes for presentation and 5 minutes for question - answer deliberations. The presentation shall be made on the following outlines:

			<ul>
				<li>Business model</li>
				<li>Differentiation strategy</li>
				<li>Market size and estimation approach</li>
				<li>Overall budget</li>
				<li>Overall returns estimation</li>
				<li>How do you reach the first customer</li>
				<li>How do you reach the 100th customer</li>
				<li>Distribution, customer relation and services</li>
				<li>Team formation and roles</li>
				<li>Cost structure</li>
				<li>revenue Schemes</li>
				<li>Risks and precautions</li>
				<li>Quantification of social improvements</li>
			</ul>
		</p>


