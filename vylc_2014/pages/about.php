	<div id="about-page" class="page">
		<p><b>Vivekananda Samiti</b>, is a student body in IIT Kanpur, that has been motivating the youth community towards the path of human excellence. It has been disseminating and trying to actualize the messages of <b>Swami Vivekananda</b> since last four decades.
		</p>
		<p>Our activities include weekly discourses on Vedanta, weekly self awakening hour, invited talks, seminars and workshops on leadership, human excellence, soft skills, Indian scientific and cultural heritage, and other youth relevant topics. Our community service activities include medical camps, eye camps, literary camps for the under-privileged sections of the society in and around the campus. In addition, we conduct collection drives for helping poor and relief victims. Collections are in the form of monetory, clothing and medicines. Further we also organize Sanskrit classes, city heritage tours, sale and exhibition of Ramakrishna Vivekananda Vedanta literature, and various competitions for school and college students.
		</p>
		<p>In particular, we have been celebrating the 150th birth anniversary of Swami Vivekananda since last 2 years and are going to hold Vivekananda Youth Leadership Convention during Jan.10-13, 2014.
		</p> 
		<p><b>Vivekananda Youth Leadership Convention</b> aims to inspire our students to imbibe and assert the human values in their lives. Our aim is to foster the spirit of enthusiasm, fearlessness, determination, self-belief, perseverance and service among our youth.</p>

		<p>With the focus on <b>leadership, social entrepreneurship,</b> and <b>promotion of Indian culture and integrity,</b> the Convention aspires to make our youth believe in their inherent potential. This is a very unique event, providing inspiration and permuting leadership among the youth who can be the torch bearers in the society tomorrow.</p><br/>

		The highlights of the Convention are the following: 
		<li>Inspirational talks by eminent leaders from various fields.</li>
		<li>Workshops on Self Expression and Enlightened Citizenship-Youth Cafe.</li>
		<li>Exciting College level events like Youth Leadership Challenge, Indian & Cultural Quotient, Vaividhya Bharata, Innovation - The Social Entrepreneur, Movie making, to name a few.</li>
		<li>Enriching School events like Quiz, Poster Making, Extempore and Essay writing.</li>
		<li>A Special Cultural Show- A Tabla performance by “Princess of Tabla”, Ms. Rimpa Shiva.</li>
