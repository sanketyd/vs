		<ul>
			<li>Inspirational talks by eminent leaders from various fields such as industry, administrative services, judiciary, education, defense, entrepreneurs, social service, literature, media, sports, to name a few. </li>
			<li>Workshop on Leadership and Soft Skills.</li>
			<li>Exciting College level competitions like Youth Leadership Challenge, Kaun Banega Champion (KBC), Stage Play and Creative Writing.</li>
			<li>Enriching School events like Quiz on Indian Culture, Extempore, Painting, Essay writing and Career Counseling, where we invite participation from over 25 schools in the city.</li>
			<li>Panel Discussion on socially relevant subjects.</li>
			<li>Community Service activities such as health camps for the under-privileged people in and around the campus.</li>
