<p id="event_4"><b>Medical Health Camp on 12<sup>th</sup> February.</b><br/>
Vivekananda Samiti, IIT Kanpur organized a Medical health camp for about 375 under-privileged children and their parents in and around the campus. This program was conducted on Sunday, 12<sup>th</sup> Febrauary 2012 9:30 am to 1 pm with the help of Ramakrishna Mission Hospital, Kanpur at Shiksha Sopan Vidyalaya at Barasirohi village, near IIT Kanpur. This camp was arranged with the help of voluntary service of a team of four competent doctors: Dr. G.K. Tiwari, Dr. R.M. Gupta, Dr. Mohita Gulati and Dr. Sutapa Mandal, all of whom offer their services regularly at Ramakrishna Mission Hospital. This camp was inaugurated by Dr. H.C. Verma, President, Shiksha Sopan and Swami Tejomayananda, Secretary, Ramakrishna Mission Hospital. Among the other eminent guests included Dr. O.P. Mishra, Health Center, IITK who provided free medicines. Over 35 comitted volunteers from Vivekananda Samiti and Shiksha sopan rendered their voluntary services to carry out the camp smoothly.
