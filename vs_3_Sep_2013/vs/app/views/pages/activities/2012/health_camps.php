<b>Medical Eye Camp on 19<sup>th</sup> February.</b><br/>
An Eye Camp was organized at Navasrijan Vidyalaya, Ratanpur near Nankari on Sunday 19th Feb., 2012 from 9:30 am to 1 pm. Dr. Anju Mishra with her team offered her voluntary service. About 75 patients came for examination. About 90% patients were found suffering from long or short-sightedness. They were provided with glasses at a subsidized price. Four Patients suffering from cataract were offered surgery and medication without any charges.
